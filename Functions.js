//Parametazied Function
//In This parametazied Funtion We Can Able to initialize some value in this function
function method2(name)
{
    let b="Hi Iam "+name+" From Variable b";
    console.log(b);
}
method2("Mathan Kumar");

//Double value loading parametarized function
function method3(Firstname,lastname)
{
    let c="Hi Iam "+Firstname+lastname+" From variable c";
    console.log(c);
}
method3("ar","rahman");


//Prototypal Inhertance
//In The Prototypal Inhertance We Can Able To call with the help of object creation and using .
//using dot . we can able to acheive the prototypal inhertance
const i=
{
    a:1,
    b:2,
    proto:
    {
        b:3,
        d:4,
    }
}
console.log(i.a)
console.log(i.b)
console.log(i.proto.b)
console.log(i.proto.d)